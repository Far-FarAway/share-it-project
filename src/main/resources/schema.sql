CREATE TABLE IF NOT EXISTS users (
    user_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    user_name VARCHAR(255) NOT NULL,
    email VARCHAR(255) UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS items (
    item_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    user_id INT REFERENCES users(user_id) ON DELETE CASCADE NOT NULL,
    item_name VARCHAR(255) NOT NULL,
    description VARCHAR(511) NOT NULL,
    available BOOLEAN DEFAULT TRUE NOT NULL,
    book_count INT DEFAULT 0 NOT NULL
);

CREATE TABLE IF NOT EXISTS bookings (
    booking_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    item_id INT REFERENCES items(item_id) ON DELETE CASCADE NOT NULL,
    booker_id INT REFERENCES users(user_id) ON DELEte CASCADE NOT NULL,
    status VARCHAR(15),
    start_date TIMESTAMP NOT NULL,
    end_date TIMESTAMP NOT NULL,
    CONSTRAINT check_date CHECK (start_date < end_date)
);

CREATE TABLE IF NOT EXISTS comments (
    comment_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    item_id INT REFERENCES items(item_id) ON DELETE CASCADE NOT NULL,
    author_name VARCHAR(255) NOT NULL,
    text VARCHAR(511),
    created TIMESTAMP NOT NULL
);